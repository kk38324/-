from turtle import *
colormode(255)
#画黑圈,以（0，0）为原点
pensize(8)
pencolor((29,24,21))
circle(80)
#移动到蓝圈位置
penup()
fd(-200)
#画蓝圈
pendown()
pencolor((0,107,176))
circle(80)
#移动到红圈位置
penup()
fd(400)
#画红圈
pendown()
pencolor((220,47,31))
circle(80)
#移动到绿圈位置
seth(-90)
penup()
fd(80)
seth(180)
fd(100)
#画绿圈
pendown()
pencolor((5,147,65))
circle(-80)
#移动到黄圈位置
penup()
fd(200)
#画黄键
pendown()
pencolor((239,169,13))
circle(-80)
#画蓝圈下侧填充
penup()
goto(-200,0)
pendown()
pencolor((0,107,176))
circle(-80,-30)
#画黑圈左侧填充
penup()
goto(0,160)
seth(180)
pencolor((29,24,21))
pendown()
circle(80,135)
#画黑圈右侧填充
penup()
goto(0,0)
seth(0)
circle(80,45)
pendown()
circle(80,45)
#画红圈左侧填充
penup()
goto(200,0)
seth(180)
pencolor((220,47,31))
circle(-80,45)
pendown()
circle(-80,45)
penup()
fd(10000)
